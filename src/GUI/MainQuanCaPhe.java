    private void lbl_SuaGGMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_lbl_SuaGGMouseClicked
        // TODO add your handling code here:
        String mahd = cbo_MaHD.getSelectedItem().toString();
        int giamgia=Integer.parseInt(txt_GiamGia.getText());
        String[] tachchuoi = HoaDonDAO.XuatGG_TongTien(mahd).split(" ");
        int tongtien = Integer.parseInt(tachchuoi[1]);
        int gg=0;
        
        if(giamgia>=0 && giamgia<=100 ){
            float kq = (float)giamgia/100*tongtien;
            gg = (int)kq;
        }
        else
            JOptionPane.showMessageDialog(this, "Giảm giá theo % chỉ có thể từ 0 đến 100 !","Thông báo",2);
        
        String sql = "update HoaDon set GiamGia = "+gg+" where MaHD = '"+mahd+"'\n"+
                         "exec tongtien_hoadon '"+mahd+"'";
        HoaDonDAO.ThemXoaSua(sql);
        XuatGTHD(mahd);
        txt_GiamGia.setText(giamgia+"");
    }//GEN-LAST:event_lbl_SuaGGMouseClicked

