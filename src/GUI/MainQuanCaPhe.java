    public void bangBan(){
        tblTitleBan.add("STT"); tblTitleBan.add("Mã bàn"); 
        tblTitleBan.add("Tên bàn"); tblTitleBan.add("Trạng thái"); 
    }
    public void laydulieuBan(ArrayList<Ban> dsb){
        tblDataBan.removeAllElements();
        int j = 0;
        for(Ban i : dsb){
            Vector v = new Vector();
            v.add(j+1); v.add(i.getMaban()); 
            v.add(i.getTenban()); v.add(i.getTrangthai());
            tblDataBan.add(v);
            j++;
        }
        tbl_Ban.setModel(new DefaultTableModel(tblDataBan, tblTitleBan));
    }
    
    public void bangHD(){
        tblTitleHD.add("STT"); tblTitleHD.add("Mã món"); tblTitleHD.add("Tên món"); 
        tblTitleHD.add("Số lượng"); tblTitleHD.add("Giá tiền"); 
        tblTitleHD.add("Thành tiền");
    }
    public void laydulieuHD(ArrayList<ChiTietHD> cthd){
        tblDataHD.removeAllElements();
        int j = 0;
        for(ChiTietHD i : cthd){
            Vector v = new Vector();
            v.add(j+1); v.add(i.getMasp()); v.add(i.getTensp()); 
            v.add(i.getSoluong()); v.add(i.getGiatien());
            v.add(i.getThanhtien());
            tblDataHD.add(v);
            j++;
        }
        tbl_HD.setModel(new DefaultTableModel(tblDataHD, tblTitleHD));
    }
