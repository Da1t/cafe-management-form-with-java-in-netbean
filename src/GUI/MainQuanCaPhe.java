    public void XuatGTHD(String mahd){
        
        String[] tachchuoi = HoaDonDAO.XuatGG_TongTien(mahd).split(" ");
        String giamgia = tachchuoi[0];
        String tongtien = tachchuoi[1];
        
        txt_TongTien.setText(tongtien);
        txt_GiamGia.setText(giamgia);
        
    }
    public void TaoCTHD(String mahd, String masp){

        String sql = "insert into ChiTietHoaDon(MaHD,MaSP) values('"+mahd+"','"+masp+"')\n" +
                     "exec TinhThanhTien '"+mahd+"', '"+masp+"'\n"+
                     "exec tongtien_hoadon '"+mahd+"'";
        int kq=HoaDonDAO.ThemXoaSua(sql);
        if(kq!=-1){
            System.out.println("Thêm sản phẩm mã "+masp+" vào hóa đơn mã "+mahd+" thành công !");
        }
        else{
            String sql_UpdateSLSP = "update ChiTietHoaDon set SoLuong = SoLuong + 1  where MaHD = '"+mahd+"' and MaSP = '"+masp+"'\n" +
                     "exec TinhThanhTien '"+mahd+"', '"+masp+"'\n"+
                     "exec tongtien_hoadon '"+mahd+"'";
            HoaDonDAO.ThemXoaSua(sql_UpdateSLSP);
        }
        dscthd = HoaDonDAO.getDSCTHD(cbo_MaHD.getSelectedItem().toString());
        laydulieuHD(dscthd);
        XuatGTHD(mahd);
    }
    private void btn_ThemMonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_ThemMonActionPerformed
        // TODO add your handling code here:
        int n = 100000;
        String mahd = "";
        String manv = cbo_MaNV.getSelectedItem().toString();
        String masp = SanPhamDAO.XuatMaSP(cbo_TenSP.getSelectedItem().toString());
        if(cbo_MaHD.getItemCount()==0)
        {

            for(int i=0;i<n;i++){
                mahd = "HD"+(i+1);
                if(HoaDonDAO.XacNhanHD(mahd)==0){
                    break;
                }
            }

            TaoHD(mahd, manv);
            cbo_MaHD.addItem(mahd);
            TaoCTHD(mahd,masp);
        }
        else{
            TaoCTHD(cbo_MaHD.getSelectedItem().toString(),masp);
        }
    }//GEN-LAST:event_btn_ThemMonActionPerformed

